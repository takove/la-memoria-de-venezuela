  <h1 class="text-4xl font-bold text-gray-900 mb-8">Acerca de La Memoria de Venezuela</h1>
  
  <div class="prose prose-lg max-w-none">
    <section class="mb-12">
      <h2>Misión</h2>
      <p>
        La Memoria de Venezuela es una plataforma de transparencia y rendición de cuentas que 
        documenta información pública sobre funcionarios del régimen venezolano (1999-2026) 
        que han sido objeto de sanciones internacionales, procesos legales, o han sido 
        documentados en informes de derechos humanos.
      </p>
    </section>

    <section class="mb-12">
      <h2>Fuentes de Datos</h2>
      <p>Toda la información proviene de fuentes oficiales y públicas:</p>
      <ul>
        <li>
          <strong>OFAC (Office of Foreign Assets Control)</strong> - 
          Departamento del Tesoro de Estados Unidos
        </li>
        <li>
          <strong>DOJ (Department of Justice)</strong> - 
          Acusaciones federales y procesamientos legales
        </li>
        <li>
          <strong>IACHR (Comisión Interamericana de Derechos Humanos)</strong> - 
          Informes sobre violaciones de derechos humanos
        </li>
        <li>
          <strong>DEA (Drug Enforcement Administration)</strong> - 
          Casos relacionados con narcotráfico
        </li>
        <li>
          <strong>Unión Europea, Reino Unido, Canadá</strong> - 
          Sanciones internacionales adicionales
        </li>
      </ul>
    </section>

    <section class="mb-12">
      <h2>Metodología</h2>
      <p>
        Esta plataforma recopila, estructura y presenta información que ya es de dominio público.
        No genera nueva información ni realiza investigaciones independientes.
      </p>
      <ol>
        <li>Recopilación de datos de fuentes oficiales gubernamentales</li>
        <li>Verificación cruzada de información entre múltiples fuentes</li>
        <li>Estructuración en base de datos relacional</li>
        <li>Presentación accesible y búsqueda facilitada</li>
      </ol>
    </section>

    <section class="mb-12">
      <h2>Limitaciones</h2>
      <ul>
        <li>Solo documentamos información de fuentes oficiales públicas</li>
        <li>No realizamos acusaciones ni juicios de valor</li>
        <li>La información se presenta tal como aparece en las fuentes originales</li>
        <li>Actualizamos los datos cuando hay cambios en las fuentes oficiales</li>
      </ul>
    </section>

    <section class="mb-12">
      <h2>Contacto</h2>
      <p>
        Para correcciones, actualizaciones, o consultas sobre los datos presentados, 
        por favor contacte al equipo a través de nuestros canales oficiales.
      </p>
    </section>

    <section class="p-6 bg-gray-100 rounded-lg">
      <h2 class="mt-0">Aviso Legal</h2>
      <p class="mb-0">
        Esta plataforma es un proyecto de documentación pública. Toda la información 
        presentada proviene de fuentes gubernamentales oficiales y reportes de 
        organizaciones internacionales de derechos humanos. El objetivo es facilitar 
        el acceso a información que ya es de dominio público, promoviendo la 
        transparencia y la rendición de cuentas.
      </p>
    </section>
